def hangman():
    print_logo()
    word = get_word()
    grid = ['_' for i in range(len(word))]
    lives = 6

    while "_" in grid and lives != -1:
        print(grid)
        guess = input("Guess a letter: ")
        for i in range(len(word)):
            if word[i] == guess and grid[i] == '_':
                grid[i] = guess
                break
        else:
            print_hangman(lives)
            lives -= 1
            
    
    if lives == -1:
        print_hangman(0) 
        return f"YOU LOSE! The word was {word}"
    else: 
        return "YOU WIN! YAYYY"

